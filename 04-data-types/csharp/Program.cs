using System;

namespace DataTypes
{
    class Program
    {
        static void Main(string[] args)
        {
            Console.WriteLine("Data Types in C#");

            // String
            var s = "this is a string"; // string s = "this is a string";

            // Boolean
            var b = true; // bool b = true

            // Integer
            var i = 1; // int i = 1

            // Double
            var f = 1.222D; // double f = 1.222;

            // Byte
            var byteValue = 0x00C9; // byteValue = 0x00C9;
        }
    }
}
